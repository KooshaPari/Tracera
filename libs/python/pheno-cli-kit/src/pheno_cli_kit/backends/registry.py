class BaseBackend(ABC):
    """
    Base class for all backend implementations.
    """

    @abstractmethod
    def generate(
        self,
        cli_name: str,
        version: str,
        help: str,
        commands: List[Command],
        global_options: List[Option],
        epilog: Optional[str] = None,
        standalone: bool = True,
    ) -> str:
        """
        Generate CLI code for this backend.
        """
        pass

